# variable and operators
# variable is a container that holds a value
# variable can be of different types: int, float, str, bool, list, tuple    
# variable can be assigned a value using the assignment operator (=)
# variable can be used to store data and perform operations on it   
# example of variable assignment
x = 10
y = 20
z = x + y
print(z)  # output: 30
# example of variable assignment with different types
name = "John"   
age = 30
is_student = True
print(name)  # output: John
print(age)   # output: 30
print(is_student)  # output: True
# example of variable assignment with list and tuple    
fruits = ["apple", "banana", "orange"]
colors = ("red", "green", "blue")   
print(fruits)  # output: ['apple', 'banana', 'orange']
print(colors)  # output: ('red', 'green', 'blue')
# operators are symbols that perform operations on variables and values
# operators can be arithmetic, comparison, logical, assignment, etc.        
# example of arithmetic operators
a = 10
b = 5
print(a + b)  # output: 15  
print(a - b)  # output: 5
print(a * b)  # output: 50  
print(a / b)  # output: 2.0
print(a % b)  # output: 0   
print(a ** b)  # output: 100000
# example of comparison operators
print(a == b)  # output: False
print(a != b)  # output: True   
print(a > b)   # output: True
print(a < b)   # output: False
print(a >= b)  # output: True
print(a <= b)  # output: False
# example of logical operators
x = True    
y = False
print(x and y)  # output: False 
print(x or y)   # output: True
print(not x)    # output: False
print(not y)    # output: True      

